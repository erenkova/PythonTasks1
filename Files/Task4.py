a=input()
with open(a, 'r') as f:
    r=f.read()
    zp = '!:;.,?'
    for i in zp:
        r.replace(i, '')
    lsd = r.split()
    for i in range(len(lsd)):
        lsd[i] = lsd[i].lower()
    mx = 0
    word = ''
    for i in lsd:
        c = lsd.count(i)
        if c > mx:
            mx = c
            word = i
    print(word)